export function findCatalystById(id: string): Catalyst | undefined {
  return CATALYSTS.find((c) => c.id === id);
}

export function findCatalystsByTicker(ticker: string): Catalyst[] {
  return CATALYSTS.filter((c) => c.ticker.toUpperCase() === ticker.toUpperCase());
}

export function tickerStackCounts(): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const c of CATALYSTS) {
    if (c.status !== "upcoming") continue;
    counts[c.ticker] = (counts[c.ticker] ?? 0) + 1;
  }
  return counts;
}
