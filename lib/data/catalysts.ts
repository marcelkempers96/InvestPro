// Anchor seed data to a stable window relative to today's date.
// In production this comes from the ingestion layer (FMP, FDA, MarketAux, etc.).
function offsetDate(daysFromNow: number): string {
  const d = new Date();
  d.setUTCHours(0, 0, 0, 0);
  d.setUTCDate(d.getUTCDate() + daysFromNow);
  return d.toISOString().slice(0, 10);
}

